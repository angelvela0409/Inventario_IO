package Entidad;

/**
 *
 * @author EQUIPO
 */
public class Bodega {
    private int id;
    private String sucursal;
    private int entrada;
    private int salida;
    private int stock;
    private String fecha;
    
 
    public Bodega() {
    }

    public Bodega(int id, String sucursal,int entrada,int salida, int stock, String fecha) {
        this.id = id;
        this.sucursal = sucursal;
        this.entrada = entrada;
        this.salida = salida;
        this.stock = stock;
        this.fecha = fecha;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSucursal() {
        return sucursal;
    }

    public void setSucursal(String sucursal) {
        this.sucursal = sucursal;
    }

    public int getEntrada() {
        return entrada;
    }

    public void setEntrada(int entrada) {
        this.entrada = entrada;
    }

    public int getSalida() {
        return salida;
    }

    public void setSalida(int salida) {
        this.salida = salida;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
    
    
}
